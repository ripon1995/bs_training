package com.example.multiscreentest;

public class Word {

    private String mWord;
    private String nWord;

    public Word(String mWord, String nWord) {
        this.mWord = mWord;
        this.nWord = nWord;
    }

    public String getmWord() {
        return mWord;
    }


    public String getnWord() {
        return nWord;
    }


}
